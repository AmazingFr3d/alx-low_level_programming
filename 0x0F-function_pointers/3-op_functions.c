/**
 * op_add - Adds 2 int values
 * @a: First Int
 * @b: Secont int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Subtracts a from b
 * @a: First Int
 * @b: Secont int
 * Return: diference of a and b
 */
